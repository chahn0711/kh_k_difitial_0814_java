package 객체를배열로관리;

public class NameCard {
    private String name;
    private String mail;
    private String phone;
    private int age;

    public void setName(String name) {
        this.name = name;
    }

    public void setMail(String mail) {
        this.mail = mail;
    }

    public void setPhone(String phone) {
        this.phone = phone;
    }

    public void setAge(int age) {
        this.age = age;
    }
}
